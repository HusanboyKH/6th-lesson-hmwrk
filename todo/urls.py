from django.urls import path
from .views import ListTaskView, DeleteTaskView, DetailTaskView, CreateTaskView, UpdatePutTaskView, UpdatePatchTaskView
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)
from django.urls import re_path
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

schema_view = get_schema_view(
    openapi.Info(
        title="Snippets API",
        default_version='v1',
        description="Test description",
        terms_of_service="https://www.google.com/policies/terms/",
        contact=openapi.Contact(email="contact@snippets.local"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('swagger<format>/', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    path('', ListTaskView.as_view()),
    path('<int:id>', DetailTaskView.as_view()),
    path('create/', CreateTaskView.as_view()),
    path('delete/<int:id>', DeleteTaskView.as_view()),
    path('update_patch/<int:id>', UpdatePatchTaskView.as_view()),
    path('update_put/<int:id>', UpdatePutTaskView.as_view()),
    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
]